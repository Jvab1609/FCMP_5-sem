    BPlayer player = new BPlayer(s, d, x, y);
    BComp comp = new BComp(sT, dT, xT, yT, this);
    
    boolean colidiu = false;
    
    @Override
    public void messageArrived(MessageEvent e) {
        colidiu = true;
    }
    
    
    
    Runnable comportamentoComp = new Runnable() {
        @Override
        public void run() {
            while (true) {
                int soma = 0;
                int[] probs = comp.getProbs();
                for (int i = 0; i < probs.length; i++) {
                    soma += probs[i];
                }
                
                int height = jPanel1.getHeight();
                int width = jPanel1.getWidth();
                int l = (int) (Math.random()*(40-1 + 1) + 1);
                int tecla = (int) (Math.random()*(soma-0 + 1) + 0);
                for (int j = 0; j < l; j++) {
                    comp.processarInput(tecla, height, width);
                    jPanel1.repaint();
                    if (colidiu == true) {
                        j = 41;
                        System.out.println("COLIDIU");
                        System.out.println("X: " + comp.getX() + "\tY: " + comp.getY() + "\tS: " + comp.getS());
                        colidiu = false;
                    }
                    try {
                        Thread.sleep(100); // dá um sleep de 0,1s porque senão os movimentos são instantâneos
                    } catch (InterruptedException ex) {
                        Logger.getLogger(ProvaB2.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
                try {
                    int t = (int) (Math.random()*(600-500 + 1) + 500); // Faz o sleep de 0,5s ou um pouquinho mais
                    Thread.sleep(t);
                } catch (InterruptedException ex) {
                }
            }
        }
    };
    
